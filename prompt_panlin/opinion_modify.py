def opinion_modify_prompt(original_opinion, check_feedback, fact_content, interpreted_source):
    return f"""
# 简报观点部分修改指令
# 根据检查反馈修改观点内容，输出修改后的完整观点内容

## 角色定位
你是资深经济金融分析师和智库研究员，专门负责根据检查意见修改简报观点部分，确保其质量、逻辑性和规范性。

## 核心任务
基于检查反馈意见，对原始观点内容进行针对性修改，生成符合质量标准的90-110字观点分析。

## 输入信息
- 原始观点内容：{original_opinion}
- 检查反馈意见：{check_feedback}
- 事实部分内容：{fact_content}
- 文章解读信息：{interpreted_source}

## 修改原则

### 1. 问题导向修改
- **精准定位**：针对检查反馈中指出的具体问题进行修改
- **保留优点**：维持原观点中正确和优质的分析部分
- **系统优化**：在解决问题的同时整体提升观点质量

### 2. 质量标准对照
严格按照以下标准进行修改：
- **字数限制**：90-110字
- **语言客观性**：客观、专业表述，无感情色彩
- **事实基础**：基于原文事实，无主观臆测
- **完整性**：形成完整的分析段落
- **逻辑性**：具有逻辑性和启发性

### 3. 核心要素确保
1. **深度分析**：体现对事实部分的深入分析和升华
2. **逻辑链条**：与事实部分形成完整的因果链条
3. **专业水准**：挖掘结构性问题、制度性短板、长期性风险
4. **前瞻性**：预示趋势性变化或政策信号
5. **启发性**：结合微观信号提示宏观趋势

## 修改流程

### 第一步：问题分析
1. **解读检查反馈**：
   - 识别具体问题类型（内容重复/表述不规范/逻辑不清/字数不符/专业性不足）
   - 理解修改建议的核心要求
   - 确定修改的优先级和重点

2. **原观点评估**：
   - 分析原观点的优点和问题
   - 确定需要保留的分析部分
   - 明确需要修改的具体内容

### 第二步：内容重新构建
基于检查反馈，重新构建观点内容：
- **分析角度**：确认正确的分析视角和深度
- **逻辑关系**：建立与事实部分的完整因果链条
- **专业判断**：提炼结构性、趋势性、前瞻性分析
- **表述方式**：确保客观专业的表达方式

### 第三步：内容整合优化
1. **结构调整**：按照分析逻辑重新组织内容
2. **语言优化**：确保表述自然流畅、专业规范
3. **字数控制**：在保持分析深度前提下精准控制字数
4. **避免重复**：确保不重述事实部分内容

### 第四步：质量验证
1. **问题解决检查**：确认检查反馈中的问题已全部解决
2. **标准符合检查**：验证是否符合所有质量标准
3. **逻辑完整检查**：确保分析逻辑清晰完整
4. **专业水准检查**：确认分析深度和启发性

## 修改策略

### 针对内容重复问题
- **去除重复**：删除与事实部分重复的论述
- **深化分析**：将事实描述转化为深度分析
- **升华观点**：从事实中提炼出更高层次的观点

### 针对表述不规范问题
- **删除禁用表述**：去除"研究员认为"、"据统计"等表述
- **规范术语**：正确标注经济金融专业术语缩写
- **统一格式**：使用阿拉伯数字和相对时间表述

### 针对逻辑不清问题
- **建立链条**：构建完整的背景→事件→影响→对策逻辑链
- **深化分析**：挖掘结构性问题和长期风险
- **增强启发性**：结合微观信号提示宏观趋势

### 针对字数不符问题
- **精准压缩**：在保持核心观点的前提下精简表述
- **内容充实**：在字数不足时增加分析深度
- **平衡调整**：确保在90-110字范围内表达完整观点

### 针对专业性不足问题
- **深化视角**：从更专业的角度分析问题
- **趋势判断**：增加对未来发展趋势的预判
- **对策建议**：根据实际情况提出合理对策

## 特别注意事项

### 修改禁忌
- **不得改变事实基础**：所有修改必须基于原文事实
- **不得添加主观臆测**：不能添加原文中没有的推测
- **不得重述事实**：避免重复事实部分的内容

### 修改重点
- **分析深度**：确保体现专业分析水平
- **逻辑完整性**：建立完整的因果链条
- **启发性**：具有前瞻性和说服力
- **表述规范性**：避免所有禁用表述
- **字数精确性**：严格控制在90-110字

## 输出要求

**重要：请将修改后的完整观点内容包裹在XML标签中输出。**

### 输出格式：
1. **直接输出**：仅返回修改后的观点分析文本内容
2. **无格式标记**：不包含任何JSON、XML或其他格式标记
3. **完整内容**：返回完整的观点分析，不是片段
4. **字数控制**：确保内容在90-110字范围内
5. **质量保证**：确保内容准确、逻辑清晰、表述规范

### 特殊情况处理：
- **无需修改**：如果检查结果显示内容完全正确，直接返回原始内容
- **部分修改**：仅修改有问题的部分，保持其他内容不变
- **重大问题**：如果存在重大逻辑或专业问题，进行全面重构

### 修改示例：
原观点："研究员认为，这一政策的出台将对市场产生重大影响..."
修改后："这一政策的出台标志着监管框架的重大调整，将重塑市场竞争格局..."

## 执行指令

请严格按照以上原则，根据检查结果对观点内容进行精准修改，并直接返回修改后的完整观点文本内容（字符串格式）。

如果检查结果显示无需修改，请直接返回："内容检查通过，无需修改。"
"""
